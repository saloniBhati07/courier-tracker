@app.route('/dashboard')
@login_required
def dashboard():
    """Admin dashboard with stats and recent updates."""
    db = get_db()
    total      = db.execute('SELECT COUNT(*) FROM shipments').fetchone()[0]
    delivered  = db.execute("SELECT COUNT(*) FROM shipments WHERE status='Delivered'").fetchone()[0]
    in_transit = db.execute("SELECT COUNT(*) FROM shipments WHERE status='In Transit'").fetchone()[0]
    pending    = db.execute("SELECT COUNT(*) FROM shipments WHERE status='Pending'").fetchone()[0]
    out_for    = db.execute("SELECT COUNT(*) FROM shipments WHERE status='Out for Delivery'").fetchone()[0]
    shipped    = db.execute("SELECT COUNT(*) FROM shipments WHERE status='Shipped'").fetchone()[0]

    recent_updates = db.execute('''
        SELECT th.*, s.tracking_id, s.receiver_name
        FROM tracking_history th
        JOIN shipments s ON th.shipment_id = s.id
        ORDER BY th.timestamp DESC LIMIT 10
    ''').fetchall()

    # Delayed: In Transit or Shipped for >5 days
    delayed = db.execute('''
        SELECT * FROM shipments
        WHERE status IN ('In Transit','Shipped','Pending')
        AND julianday('now') - julianday(created_at) > 5
        ORDER BY created_at ASC
    ''').fetchall()

    stats = {
        'total': total, 'delivered': delivered, 'in_transit': in_transit,
        'pending': pending, 'out_for': out_for, 'shipped': shipped
    }
    return render_template('dashboard.html', stats=stats,
                           recent_updates=recent_updates, delayed=delayed)

