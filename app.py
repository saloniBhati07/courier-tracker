@app.route('/api/track/<tracking_id>')
def api_track(tracking_id):
    """JSON API endpoint for tracking."""
    db = get_db()
    shipment = db.execute(
        'SELECT * FROM shipments WHERE tracking_id=?', (tracking_id,)
    ).fetchone()
    if not shipment:
        return jsonify({'error': 'Not found'}), 404
    history = db.execute(
        'SELECT * FROM tracking_history WHERE shipment_id=? ORDER BY timestamp ASC',
        (shipment['id'],)
    ).fetchall()
    return jsonify({
        'shipment': dict(shipment),
        'history': [dict(h) for h in history]
    })

