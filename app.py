@app.route('/add-shipment', methods=['GET', 'POST'])
@login_required
def add_shipment():
    """Add a new shipment."""
    if request.method == 'POST':
        import uuid
        tracking_id   = 'TRK' + uuid.uuid4().hex[:8].upper()  # Auto-generate tracking ID
        sender_name   = request.form.get('sender_name', '').strip()
        receiver_name = request.form.get('receiver_name', '').strip()
        origin        = request.form.get('origin', '').strip()
        destination   = request.form.get('destination', '').strip()
        sender_contact   = request.form.get('sender_contact', '').strip()
        receiver_contact = request.form.get('receiver_contact', '').strip()
        weight        = request.form.get('weight', '').strip()
        description   = request.form.get('description', '').strip()

        db = get_db()
        db.execute('''
            INSERT INTO shipments
            (tracking_id, sender_name, receiver_name, origin, destination,
             sender_contact, receiver_contact, weight, description, status, created_at)
            VALUES (?,?,?,?,?,?,?,?,?,'Pending', datetime('now'))
        ''', (tracking_id, sender_name, receiver_name, origin, destination,
              sender_contact, receiver_contact, weight, description))
        # Get last inserted shipment id
        shipment_id = db.execute('SELECT last_insert_rowid()').fetchone()[0]
        # Log initial tracking history
        db.execute('''
            INSERT INTO tracking_history (shipment_id, status, location, notes, timestamp)
            VALUES (?, 'Pending', ?, 'Shipment created', datetime('now'))
        ''', (shipment_id, origin))
        db.commit()
        flash(f'Shipment created! Tracking ID: {tracking_id}', 'success')
        return redirect(url_for('manage_shipments'))

    return render_template('add_shipment.html')

@app.route('/manage-shipments')
@login_required
def manage_shipments():
    """List all shipments with search and filter."""
    db = get_db()
    search = request.args.get('search', '').strip()
    status_filter = request.args.get('status', '').strip()

    query = 'SELECT * FROM shipments WHERE 1=1'
    params = []
    if search:
        query += ' AND (tracking_id LIKE ? OR sender_name LIKE ? OR receiver_name LIKE ?)'
        params += [f'%{search}%', f'%{search}%', f'%{search}%']
    if status_filter:
        query += ' AND status = ?'
        params.append(status_filter)
    query += ' ORDER BY created_at DESC'

    shipments = db.execute(query, params).fetchall()
    return render_template('manage_shipments.html', shipments=shipments,
                           search=search, status_filter=status_filter)

@app.route('/update-status/<int:shipment_id>', methods=['GET', 'POST'])
@login_required
def update_status(shipment_id):
    """Update shipment status and log to history."""
    db = get_db()
    shipment = db.execute('SELECT * FROM shipments WHERE id=?', (shipment_id,)).fetchone()
    if not shipment:
        flash('Shipment not found.', 'error')
        return redirect(url_for('manage_shipments'))

    if request.method == 'POST':
        new_status = request.form.get('status')
        location   = request.form.get('location', '').strip()
        notes      = request.form.get('notes', '').strip()

        db.execute('UPDATE shipments SET status=? WHERE id=?', (new_status, shipment_id))
        db.execute('''
            INSERT INTO tracking_history (shipment_id, status, location, notes, timestamp)
            VALUES (?, ?, ?, ?, datetime('now'))
        ''', (shipment_id, new_status, location, notes))
        db.commit()
        flash('Shipment status updated successfully!', 'success')
        return redirect(url_for('manage_shipments'))

    history = db.execute(
        'SELECT * FROM tracking_history WHERE shipment_id=? ORDER BY timestamp DESC',
        (shipment_id,)
    ).fetchall()
    return render_template('update_status.html', shipment=shipment, history=history)

@app.route('/delete-shipment/<int:shipment_id>', methods=['POST'])
@login_required
def delete_shipment(shipment_id):
    """Delete a shipment and its history."""
    db = get_db()
