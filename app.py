@app.route('/track', methods=['GET', 'POST'])
def track():
    """Public tracking page."""
    shipment = None
    history  = []
    tracking_id = request.args.get('tracking_id', '').strip()

    if tracking_id:
        db = get_db()
        shipment = db.execute(
            'SELECT * FROM shipments WHERE tracking_id=?', (tracking_id,)
        ).fetchone()
        if shipment:
            history = db.execute(
                'SELECT * FROM tracking_history WHERE shipment_id=? ORDER BY timestamp ASC',
                (shipment['id'],)
            ).fetchall()
        else:
            flash('No shipment found with that tracking ID.', 'error')

    return render_template('track.html', shipment=shipment,
                           history=history, tracking_id=tracking_id)

